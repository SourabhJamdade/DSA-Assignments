#include <iostream>
using namespace std;

int main()
{
    long long a, b;
    cin >> a;
    cin >> b;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    cout << endl;
    cout << a << " / " << b << " = " << a / b << endl;
    cout << endl;
    cout << a << " % " << b << " = " << a % b << endl;
}